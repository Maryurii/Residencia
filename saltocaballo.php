										    	<p>
										    	En esta lección aprenderás como hacer un flik flak adelante sobre el potro gimnasia artística. Antes de que pruebes esto, debes aprender la parada de manos y todos los ejercicios básicos para saltos frontales. Asegurate que el área llegada sea protegido con colchones y pregunta a tu instructor para ayudarte.
