										    	<p>
										    	Este es un ejercicio fundamental y necesario para aprender el flik flak adelante sobre el potro de gimnasia artística. Esta es la primera vez que solo tus manos tocaran el potro. Asegurate que tu entrenador este cerca de la mesa, cuando intentes hacer este ejercicio por primera vez.
										    	</p>
										    	<ul>
									             	<li><i class="icofont-check-circled"></i> Para realizar este ejercicio, coloca un montón de colchones a la altura del potro.</li>
									             	<li><i class="icofont-check-circled"></i> Corre hacia la mesa y balancea los brazos desde tu espalda hacia adelante, igual que en los ejercicios anteriores de salto de potro.</li>
									             	<li><i class="icofont-check-circled"></i> Salta con los pies juntos encima del trampolín y balancea los brazos hacia adelante y arriba. (mira los ejercicios anteriores)</li>
									              	<li><i class="icofont-check-circled"></i> Rebota en el trampolín, balancea los brazos hasta llegar a la parte plana de la mesa. Los brazos, la parte superior del cuerpo y las piernas deben estar alineados y totalmente extendidos. Las manos no deben tocar la mesa antes de que el cuerpo este a 45° grados de la lengua de la mesa.</li>
									              	<li><i class="icofont-check-circled"></i> Luego haz a una parada de de gimnasia (perfectamente recta). Es recomendable que tu entrenador este cerca de la mesa, en caso de deslizarte o caer. Entiza tus manos para lograr un mejor agarre sobre la lengua. </li>
									              	<li><i class="icofont-check-circled"></i> Después tienes que caer de espalda sin doblar tu cuerpo. Manten la postura recta de la parada de manos hasta que tus piernas y tu espalda toquen los colchones. Manten tus piernas, la parte superior de tu cuerpo y tus brazos totalmente extendidos hasta que caigas. Asegurate que los colchones y la lengua del potro estén a la misma altura. </li>

									              	<h3> Consejo </h3>
									              	<li><i class="icofont-check-circled"></i> Entiza tus manos para lograr un mejor agarre y control sobre la lengua (con tiza y agua). </li>
									              	<li><i class="icofont-check-circled"></i> Si intentas hacer este ejercicio por primera vez, tu entrenador o un gimnasta experimentado debe estar cerca del potro para ayudarte y protegerte. </li>
									              	<li><i class="icofont-check-circled"></i>  Asegurate que el montón de colchones tenga exactamente la misma altura del potro. </li>
									              	<li><i class="icofont-check-circled"></i> Aprende todos los ejercicios anteriores del salto de potro de gimnasia y la parada de manos en el suelo. (balanceo de manos) (ejercicio 1) (ejercicio 2) (ejercicio 3) (parada de manos) </li>
									              	<li><i class="icofont-check-circled"></i> Al principio debes ir a una parada de manos y luego dejarte caer. Más tarde puedes empujarte un poco con las manos, de modo que tus manos dejen la mesa por un momento y vueles más adelante. Pero aún llegas con la espalda como en la primera variación (1). De esta forma te acostumbrarás a volar hacia adelante - lejos de la mesa. Esto te será de utilidad cuando quites los colchones. </li>
