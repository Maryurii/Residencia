										    	<h3> Gimnasia Artística - Flik Flak Adelante </h3>
										    	<p>
										    	El flik flak adelante es como la vuelta de manos solo que saltas con ambas piernas a la vez. Salta adelante con ambas piernas, pon tus manos en el suelo como en una posición de parada de manos y azota tus piernas extendidas hacia arriba. Pon tus piernas otra vez sobre el suelo. Es necesario aprender el flik flak adelante antes de intentar el mortal adelante extendido. Antes que intentes el flik flak adelante, debes ser capaz de hacer el salto pez y la vuelta de manos a dos pies. 
										    	</p>
										    	<ul>
									             	<li><i class="icofont-check-circled"></i> <b> Corre adelante - Salta desde la punta de tus pies. </b> Puedes hacer también el flik flak adelante después de una vuelta de manos o después de otro flik flak adelante. La variación más dificultosa es hacer el flik flak adelante y después un salto mortal adelante. Conserva tus codos extendidos durante toda la técnica. La parte superior de tus hombros se quedan junto a las orejas todo el tiempo ...Los brazos y la cabeza se mueven como una unidad.</li>
									             	<li><i class="icofont-check-circled"></i> Cuando saltes desde la puntas de tus pies, tus caderas no deben estar atrás del sitio donde tus pies toquen el suelo! Salta muy lejos hacia adelante y conserva tus codos extendidos. No dobles mucho tus rodillas a medida que tu saltes lejos. El despegue es dinámico.</li>
									             	<li><i class="icofont-check-circled"></i> Manten tus abdominales firmes y redondea tu espalda un poco (pero no demasiado). También puedes mantener tu espalda recta siempre y cuando mantengas tus abdominales firmes. Pero no ahueques tu espalda. </li>
									              	<li><i class="icofont-check-circled"></i> No pongas tus manos demasiado pronto o cerca de donde saltaste. Ten prensente que tus brazos deben permanecer alineados con la parte superior de tu cuerpo. Es como si alguien lanzará un palo hacia delante, que primero llega al suelo despues de una rotación de 180° y luego se para recta , y hace otra rotación de 180°. </li>
									              	<li><i class="icofont-check-circled"></i> Llega con tus manos (separa tus manos a una distancia de un hombro ancho) y luego patea tus piernas hacia arriba sin doblar tus rodillas. Mira en dirección a tu manos, extiende tu cuerpo y ve a una posición de parada de manos.</li>
									              	<li><i class="icofont-check-circled"></i> Luego empuja tus manos hacia atrás y abajo, ahueca tu espalda. Sigue mirando hacia el piso lo más que puedas. Asegurate que tus codos, rodillas y tus tobillos esten totalmente extendidos. Debe sentirse como si patearas tus piernas a 45° adelante y arriba - no como si solo patearas tus piernas adelante!</li>
									              	<li><i class="icofont-check-circled"></i> Vuela hacia adelante y manten tus brazos alineados con la parte superior de tu cuerpo. No habras los brazos a los lados, cuando hagas el flik flak. Manten la mirada en tus manos.</li>
									              	<li><i class="icofont-check-circled"></i> Luego llega con la punta de tus pies y mira 45° arriba. Tus manos deben apuntar arriba, tus brazos y piernas deben estar extendidos.</li>
									              	<li><i class="icofont-check-circled"></i> <b> Ejercicio adicional de gimnasia para el flik flak:</b> Rebota con la punta de tus pies, trata de incluir una rola frontal, parada de manos y otro flik flak.</li>

									              	<h3> Consejo </h3>
									              	<li><i class="icofont-check-circled"></i> No puedo dejar de decirte que: Trates de mantener tu cuerpo extendido y que no pongas tus manos demasiado pronto o cerca de donde saltaste. Un flik flak es como salto pez en el cual no rolas despues de tocar el suelo con las manos. En vez de eso extiendes tu cuerpo entero y azotas tus piernas encima y (no solo encima). </li>
									              	<li><i class="icofont-check-circled"></i> Intenta hacer 2 flik flak seguidos. </li>
									              	<li><i class="icofont-check-circled"></i> No te agaches cuando saltes. </li>
									              	<li><i class="icofont-check-circled"></i> Practica tu flik flak en colchonetas de gimnasia. Corre y rebota en el piso de gimnasia o en un reuther , pon tus manos en colchonetas y llega en las mismas. </li>
													<li><i class="icofont-check-circled"></i> <b>Métodos de entrenamiento:</b> </li>
									              	<li><i class="icofont-check-circled"></i> Haz una parada de manos en colchonetas de gimnasia y dejate caer ( con el cuerpo totalmente extendido).</li>
									              	<li><i class="icofont-check-circled"></i> Practica la vuelta de manos adelante de gimnasia.</li>
									              	<li>Practica el salto pez de gimnasia.</li>
									              	<li><i class="icofont-check-circled"></i> Salta desde un reuther o trampolín a un montón de colchonetas. </li>
									              	<li><i class="icofont-check-circled"></i> Parate encima de algo que este a 1 metro de altura y salta desde ahí a un trampolín. Luego haz un flik flak en cima de un montón de colchonetas. De esta manera puedes concentrarte en ir adelante, en vez de atrás. Ten presente que tus pies tienen que estar detrás del nivel de las caderas cuando saltes del trampolín. </li>
									              	<li><i class="icofont-check-circled"></i> Graba tus flik flak. Y mira si tus saltos estan rectos y altos. </li>

