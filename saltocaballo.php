										    	<ul>
									             	<li><i class="icofont-check-circled"></i> Corre hacia la mesa de salto y salta del trampolín hacia la mesa. Extiende tus brazos y tu cuerpo cuando estés en el aire. Bloquea (eso es cuando tus manos tocan la lengua para empujarte) poco antes que tu cuerpo alcance el nivel vertical. Conserva tus brazos totalmente extendidos y empuja tus hombros hacia arriba. Para más información sobre la primera fase del flik sobre el potro, por favor visite ejercicio 04 - salto. El bloque convierte la energía horizontal (corrida) en una energía-vertical, para que puedas saltar más alto. Si bloqueas demasiado temprano, tu salto podría ser demasiado corto y podrías golpearte la nuca con la mesa. Si tú bloqueas demasiado tarde, no saltarás muy alto.</li>
									             	<li><i class="icofont-check-circled"></i> <b>El Bloqueo: </b> Empuja tus tus brazos y hombros hacia el potro de gimnasia. Mira la instrucción del flik flak adelante en el suelo. Ten presente que los codos, las rodillas y los tobillos tienen que quedarse totalmente extendidos. El cuerpo superior, brazos y la cabeza se mueven como una unidad. Abdominales contraídos y las orejas pegadas a los hombros. Mientras mas rápido corras mejor y más alto saldrá tu salto. La primera mitad del salto no debe ser demasiado alto. Los peores errores: Los codos y rodillas doblados.</li>
