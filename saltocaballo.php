										    	<p>
										    	El salto de caballo es un disciplina de gimnasia realizada por hombres y mujeres. En la ilustración tu puedes ver cómo preparar la mesa de salto, el trampolín y las colchonetas.
										    	</p>
										    	<ul>
									             	<li><i class="icofont-check-circled"></i> Para hacer el salto, el gimnasta corre en una pista de 25 metros y después salta desde un reuther encima del caballo, donde realiza el bloqueo con las manos y después hace mortales y giros al otro lado del caballo.</li>
									             	<li><i class="icofont-check-circled"></i> La primera mitad del salto siempre es más corta que la segunda mitad y puede ser recto (como una vuelta de manos) , girando 90 grados (como una carreta) o a 180 grados (como un rondat).</li>
									             	<li><i class="icofont-check-circled"></i> Si tú corres más rápido tu salto será mas alto. Un pequeño movimiento de hombro te ayudará a impulsarte mas arriba de la mesa.</li>
