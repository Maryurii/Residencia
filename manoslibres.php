									             	<li><i class="icofont-check-circled"></i> Flexiona el tronco poco a poco con la ayuda de tu propia inercia y apoya las manos en el suelo.</li>
									              	<li><i class="icofont-check-circled"></i> Seguidamente debes elevar los pies hasta quedarte invertido con las piernas estiradas. La clave será controlar la fuerza y la inercia que ejerces para quedarte en el medio.</li>
									              	<li><i class="icofont-check-circled"></i> Un truco es empezar realizando el ejercicio junto a una pared de modo que puedas apoyar los pies sobre ella cuando estés invertido.</li>
									              	
			            						</ul>
