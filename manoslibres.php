										    	<h3> 7. Anillas de gimnasia invertidas </h3>
										    	<p>
										    	Para este ejercicio, que hoy en día se incluye en la modalidad de Crossfit, será necesario tener cautela y progresar poco a poco ya que requiere técnica y fuerza para no lesionarse los hombros.
										    	</p>
										    	<ul>
