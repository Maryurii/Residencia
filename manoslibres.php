										    	<h3> 8. Balanceo en anillas </h3>
										    	<p>
										    	El balanceo en anillas es un ejercicio que requiere progresión debido al medio inestable. No olvides que nuestra articulación del hombro es muy inestable con lo que añadiendo mas inestabilidad podría ser un movimiento con riesgo de lesión. Para evitar esto realiza el ejercicio con una buena técnica siguiendo estos pasos:
										    	</p>
										    	<ul>
									             	<li><i class="icofont-check-circled"></i> Agarra los aros y mantén tus brazos estirados.</li>
									             	<li><i class="icofont-check-circled"></i> Desde la posición anterior mantén las piernas estiradas y juntas.</li>
									             	<li><i class="icofont-check-circled"></i> Ahora, comienza a balancear tu cuerpo de atrás adelante cogiendo inercia.</li>
									             	<li><i class="icofont-check-circled"></i> Durante el movimiento, controla la hiperextensión lumbar.</li>
									              	
			            						</ul>
