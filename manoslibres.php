									             	<li><i class="icofont-check-circled"></i> Seguidamente mueve tu cuerpo de adelante a atrás hasta que poco a poco vayas invirtiendo tu cuerpo.</li>
									             	<li><i class="icofont-check-circled"></i> Mantén tus brazos estirados y al llegar a la posición invertida controla la posición.</li>
									             	<li><i class="icofont-check-circled"></i> Vuelve a la posición inicial lentamente.</li>
									              	
			            						</ul>
