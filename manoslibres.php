										    	<p>
										    	Este ejercicio dinámico será beneficioso a la hora de mejorar la musculatura y la conciencia de la posición corporal.
										    	</p>
										    	<ul>
									             	<li><i class="icofont-check-circled"></i> Desde la posición de rodillas mantén tus piernas juntas.</li>
									             	<li><i class="icofont-check-circled"></i> Estira los brazos hacia adelante apoyándolos en el suelo y realiza un movimiento de atrás hacia adelante.</li>
									             	<li><i class="icofont-check-circled"></i> Flexiona el cuello y agrupa tu cuerpo rodando hacia adelante.</li>
									             	<li><i class="icofont-check-circled"></i> Puedes finalizar el movimiento poniéndote de pie.</li>
									              	<li><i class="icofont-check-circled"></i> Algunas variantes pueden ser ponerse de pie con una pierna o realizar la voltareta desde cunclillas o de pie.  </li>
			            						</ul>
