									             	<li><i class="icofont-check-circled"></i> Inclina tu tronco a un lado y apoya las manos en el suelo para desplazarte hacia el lado contrario apoyando en el suelo con la otra pierna.</li>
									              	<li><i class="icofont-check-circled"></i> Mantener las piernas estiradas será fundamental.</li>
									              	
			            						</ul>
